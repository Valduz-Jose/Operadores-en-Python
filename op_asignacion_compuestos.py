# Operadores de Asignacion Compuestos 
# Los operadores de asignacion compuestos son una forma abreviada de escribir una operacion aritmetica seguida de una asignacion.
# += -= *= /= //= **= %=

a,b = 10,15
print(f"El valor de a es: {a} y el valor de b es: {b}")

a += b # a = a + b
print(f"El valor de a es: {a} y el valor de b es: {b}")

a -= b # a = a - b
print(f"El valor de a es: {a} y el valor de b es: {b}")

a *= b # a = a * b
print(f"El valor de a es: {a} y el valor de b es: {b}")

a /= b # a = a / b
print(f"El valor de a es: {a} y el valor de b es: {b}")